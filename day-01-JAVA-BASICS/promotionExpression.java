public class promotionExpression {
    public static void main(String[] args) {
        // conversion of small data type to int
        byte a= 1 ;
        short b = 32;
        char x = 'c' ;
        int ans = a+b+x ;
        System.out.println(ans);

        //conversion of long , float , int to double 
        long p = 343 ;
        float q = 43.04f ;
        int r = 33;
        double s = p+q+r ;
        System.out.println(s);
    }
}
